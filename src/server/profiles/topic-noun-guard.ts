export function extractSalientNouns(text: string): Set<string> {
  const tokens = text
    .toLowerCase()
    .split(/[^\p{L}]+/u)
    .filter((t) => t.length >= 4 && !STOPWORDS.has(t));
  return new Set(tokens);
}

export function assertionLeaksTopic({
  assertion,
  brief,
  profileGeneralText,
}: {
  assertion: string;
  brief: { topic: string; goal: string; notes: string };
  profileGeneralText: string;
}): boolean {
  const assertionNouns = extractSalientNouns(assertion);
  const briefNouns = extractSalientNouns(`${brief.topic} ${brief.goal} ${brief.notes}`);
  const profileNouns = extractSalientNouns(profileGeneralText);

  for (const token of assertionNouns) {
    if (!briefNouns.has(token) && !profileNouns.has(token)) {
      return true;
    }
  }
  return false;
}
